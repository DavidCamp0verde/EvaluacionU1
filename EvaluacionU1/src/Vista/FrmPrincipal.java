    public void cargarTabla(){
        if(ac != null){
            mta.setListadoAtenciones(ac);
            tblListadoAtenciones.setModel(mta);
            tblListadoAtenciones.updateUI();
        }
    }
    
    public void agregarAtencion(){
        Atencion a = new Atencion();
        Persona p = new Persona();
        if(txtAsunto.getText().isEmpty() || txtApellidosRes.getText().isEmpty() || txtNombresRes.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Agregue un asunto","Error", JOptionPane.ERROR_MESSAGE);
        }else{
            a.setAsunto(txtAsunto.getText());
            a.setNroAtenciones(a.getPersonas().getSize());
            p.setApellidos(txtApellidosRes.getText());
            p.setNombres(txtNombresRes.getText());
            a.setResponsable(p);
            ac.getAtenciones().insertar(a);
            cargarTabla();
            
        }
    }
    
    public void agregarPersona(){
        diaPersona = new DialogPersona(this, true);
        diaPersona.setVisible(true);
    }
    
